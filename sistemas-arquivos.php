                    <h2>Definição de Gestão Documental</h2>

                    <p>O decreto define a criação do SAMSP, uma rede que integra todos os arquivos e documentos da
                        administração pública municipal. Esse sistema visa garantir a coordenação e padronização das
                        ações de gestão documental, além de assegurar o acesso público a documentos que não sejam
                        sigilosos.
                    </p>

                    <p class="txt-img">O Sistema de Arquivos do Município de São Paulo deve incluir um conjunto de
                        normas técnicas e
                        operacionais para a organização, preservação e gestão de documentos nos órgãos municipais. Ele
                        está intimamente ligado ao ARQUIP (Arquivo Público Municipal), que é responsável pela
                        coordenação e implementação dessas políticas como Órgão Central.
                    </p>

                    <p>O decreto estabelece a política de gestão de documentos na administração pública municipal, com o
                        intuito de garantir que os documentos sejam produzidos, organizados, acessíveis, preservados e,
                        quando necessário, eliminados de maneira adequada. Ele abrange tanto os documentos físicos
                        quanto os digitais, promovendo a integração entre os diversos órgãos da administração municipal
                        para a gestão eficiente dos arquivos.
                    </p>
                </div>
