    <section class="linha-do-tempo">
        <div class="timeline">

            <!-- 1913 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>1913</h2>
                    <p>Ato nº 573, de 16/04/1913, reorganiza as repartições da Prefeitura e cria a Directoria do Patrimonio, Estatistica e Archivo Municipal.</p>
                    <img src="/assets/banner/historico/1913.png">
                </div>
            </div>

            <!-- 1935 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>1935</h2>
                    <p>Ato Prefeito - PREF nº 897 de 25 de julho de 1935 - Desdobra o expediente da chefia da divisão de documentação histórica e social, bem como da seção de documentação histórica.</p>
                    <img src="/assets/banner/historico/1935.png">
                </div>
            </div>

            <!-- 1947 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>1947</h2>
                    <p>O "Distúrbio Urbano de 1947" foi um protesto contra o alto custo de vida e o aumento da tarifa de transporte, que resultou na depredação da Prefeitura na Rua Líbero Badaró. Os processos públicos armazenados ali sofreram danos e foram transferidos para o Ibirapuera.</p>
                    <img src="/assets/banner/historico/1947.png">
                </div>
            </div>

            <!-- 1967 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>1967</h2>
                    <p>A gestão de processos na Prefeitura de São Paulo começou em 1967 com a criação do DAMU (Departamento de Administração do Município), por meio da Lei nº 7.057/67. O Decreto nº 7.307/67 regulamentou os serviços de contrato, protocolo e arquivo, iniciando esforços para centralizar processos em um arquivo único e informar os cidadãos sobre seu andamento.</p>
                    <img src="/assets/banner/historico/1967.png">
                </div>
            </div>

            <!-- 1977 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>1977</h2>
                    <p>Em 1977, o prefeito Olavo Egydio Setúbal criou, pelo Decreto nº 14.516/77, a Divisão do Sistema Operacional de Processos, subordinada ao EXP da Secretaria de Serviços Internos, para gerenciar recebimento, registro, movimentação, microfilmagem e arquivo de documentos administrativos. A sede foi transferida do Ibirapuera para a Rua Balsa, na Freguesia do Ó.</p>
                    <img src="/assets/banner/historico/1977.png">
                </div>
            </div>

            <!-- 1978 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>1978</h2>
                    <p>O Sistema de Gerenciamento e Controle de Processos - SCGP, criado pela Lei nº 8.777/78, informatizou o controle de processos, antes feito manualmente. O Departamento de Expediente - EXP digitava mais de 15.000 recibos diários de tramitação de processos, gerava relatórios para gestores e atendia munícipes com pedidos de vistas ou cópias de processos, agilizando a administração municipal.</p>
                    <img src="/assets/banner/historico/1978.png">
                </div>
            </div>

            <!-- 1990 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>1990</h2>
                    <p>Em 1990, a prefeita Luiza Erundina, pelo Decreto nº 28.621/90, renomeou o Departamento de Administração do Município (DAMU) para Departamento Administrativo Financeiro (DAF), criando as divisões DAF-3 (Sistema Operacional de Processos) e DAF-4 (Arquivo Geral de Processos). Pela Portaria nº 039/PREF/1990, instituiu a Comissão Central de Avaliação de Documentos (CCAD) e as Comissões Setoriais de Avaliação (CSA).</p>
                    <img src="/assets/banner/historico/1990.png">
                </div>
            </div>

            <!-- 1997 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>1997</h2>
                    <p>Após anos de planejamento, a administração municipal adotou o Sistema Municipal de Processos - SIMPROC, com operação descentralizada. Protocolos atualizavam diretamente o andamento dos processos no sistema, eliminando as fichas de carga e centralizando o atendimento ao cidadão pelo Disque-processo.</p>
                    <img src="/assets/banner/historico/1997.png">
                </div>
            </div>

            <!-- 2001 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>2001</h2>
                    <p>A prefeita Marta Suplicy altera, por meio do Decreto nº 41.290/01, a denominação da Secretaria Municipal de Administração - SMA, para Secretaria Municipal de Gestão Pública - SGP. Com a alteração, a Divisão do Sistema Operacional de Processos - DAF-3, passa a se chamar Divisão Técnica de Processos Municipais. Também foi criada a página de consulta a processos na internet, uma inovação, considerando que a internet ainda era uma novidade em nosso país.</p>
                    <img src="/assets/banner/historico/2001.png">
                </div>
            </div>

            <!-- 2002 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>2002</h2>
                    <p>Em 2002, a pedido da Ouvidoria, foi realizada a maior auditoria de processos da história da Prefeitura de São Paulo, com a digitalização de cerca de 60 mil processos e a identificação de 26 mil desaparecidos. No mesmo ano, o Decreto nº 42.431 instituiu a Tabela de Temporalidade de Processos, definindo prazos para guarda intermediária e autorizando descarte após vencimento, marcando um avanço no gerenciamento do Arquivo Geral.</p>
                    <img src="/assets/banner/historico/2002.png">
                </div>
            </div>

            <!-- 2003 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>2003</h2>
                    <p>A Secretaria Municipal de Gestão Pública - SGP mudou-se do antigo prédio, na avenida Paulista, para a rua Líbero Badaró, no centro da cidade, modernizando a estrutura física dos departamentos. Nesse ano, também, ocorreu a transição da gestão e execução dos trabalhos da Divisão do Sistema Operacional de Processos DAF-3, para controle de servidores da administração direta, até então era executado por profissionais cedidos pela PRODAM.</p>
                    <img src="/assets/banner/historico/2003.png">
                </div>
            </div>

            <!-- 2004 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>2004</h2>
                    <p>Foram iniciados estudos para a viabilização de um sistema de processo eletrônico com apoio da Universidade de São Paulo - USP. A ferramenta previa a integração de todos os sistemas necessários à instrução de um processo, a comunicação e a interação entre equipes de trabalho. Esse sistema foi batizado de SIMPAD - Sistema Municipal de Processos Administrativos Digitais, porém, era um investimento alto e complexo demais para ser executado com os recursos da época.</p>
                    <img src="/assets/banner/historico/2004.png">
                </div>
            </div>

            <!-- 2005 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>2005</h2>
                    <p>Em 2005, a Secretaria Municipal de Gestão Pública teve seu nome alterado para Secretaria Municipal de Gestão - SMG, assim foi criada a Coordenadoria do Governo Eletrônico e Gestão da Informação - CGEGI, por meio do Decreto nº 45.683. A coordenadoria reuniu as equipes de atendimento ao cidadão, Portal da Prefeitura e publicações oficiais do município. As divisões com as siglas DAF passam a se chamar DPM e DAMP e Assistência Técnica do Diário Oficial foi incorporada a DPM.</p>
                    <img src="/assets/banner/historico/2005.png">
                </div>
            </div>

            <!-- 2006 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>2006</h2>
                    <p>A Lei nº 14.141 atualizou a legislação de 1978 sobre o Processo Administrativo na Administração Pública Municipal, alinhando-a à Constituição Federal e às leis de processos administrativos Federal e Estadual, além de incluir requisitos mínimos para processos eletrônicos. Apesar de novas tentativas de implementar processos eletrônicos na Prefeitura de São Paulo (PMSP), os sistemas disponíveis não atendiam à complexidade da estrutura municipal.</p>
                    <img src="/assets/banner/historico/2006.png">
                </div>
            </div>

            <!-- 2010 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>2010</h2>
                    <p>Em 2010, a Secretaria Municipal de Gestão se fundiu com a Secretaria de Planejamento - SEMPLA, por meio do Decreto nº 51.280. A CGEGI teve seu nome alterado para Departamento de Governo Eletrônico e Tecnologia da Informação e Comunicação - DGTIC, a Divisão Técnica de Processos Municipais - DPM foi alterada para Divisão Técnica dos Processos Municipais - DGTIC-2 e a Divisão do Arquivo Municipal de Processos para DGTIC-3.</p>
                    <img src="/assets/banner/historico/2010.png">
                </div>
            </div>

            <!-- 2011 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>2011</h2>
                    <p>Com a criação do Departamento de Gestão de Documentos Públicos (DGDP), pelo Decreto nº 52.269, o controle de processos ganhou mais importância. O SIMPROC foi atualizado para SIMPROC WEB, com novo visual, funcionalidades mais práticas e redução do uso de papel. As listagens de assuntos e unidades foram substituídas pela consulta direta no sistema.</p>
                    <img src="/assets/banner/historico/2011.png" class="img-logo">
                </div>
            </div>

            <!-- 2015 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>2015</h2>
                    <p>O Departamento de Gestão de Documentos Públicos - DGDP passa a ter uma nova nomenclatura, Coordenação de Gestão de Documentos Públicos - CGDP.</p>
                    <img src="/assets/banner/historico/2015.png">
                </div>
            </div>

            <!-- 2017 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>2017</h2>
                    <p>A antiga estrutura organizacional trazida desde 1991 foi suprimida, criando, dessa forma, a Coordenadoria de Gestão Documental - CGDOC por meio do Decreto nº 57.775/2017.</p>
                    <img src="/assets/banner/historico/2017.png">
                </div>
            </div>

            <!-- 2021 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>2021</h2>
                    <p>Implementa a Política Municipal de Gestão Documental (PGDOC), instituida pelo Decreto Municipal nº 57.783, de 13 de julho de 2017, e dá outra providências. PORTARIA SECRETARIA DE GOVERNO MUNICIPAL - SGM/SEGES/CGDOC Nº 1 DE 24 DE MAIO DE 2021 « Catálogo de Legislação Municipal (prefeitura.sp.gov.br).</p>
                    <img src="/assets/banner/historico/2021.png">
                </div>
            </div>

            <!-- 2022 -->
            <div class="timeline-item right">
                <div class="content">
                    <h2>2022</h2>
                    <p>A Coordenadoria de Gestão Documental - CGDOC, da Secretaria Executiva de Gestão, da Secretaria de Governo Municipal, passa a denominar-se Arquivo Público Municipal "Jornalista Paulo Roberto Dutra" - ARQUIP. <br> DECRETO Nº 61.202 DE 1 DE ABRIL DE 2022 « Catálogo de Legislação Municipal (prefeitura.sp.gov.br)</p>
                    <img src="/assets/banner/historico/2022.png">
                </div>
            </div>

            <!-- 2023 -->
            <div class="timeline-item left">
                <div class="content">
                    <h2>2023</h2>
                    <p>Decreto nº 62.208 de 28 de fevereiro de 2023 dispõe sobre a reorganização da Secretaria Municipal de Gestão - SEGES, bem como altera a lotação e a denominação dos cargos de provimento em comissão que especifica.</p>
                    <img src="/assets/banner/historico/2023.png">
                </div>
            </div>
        </div>
