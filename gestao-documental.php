                                A capacitação de servidores e gestores para a gestão documental é fundamental, e a portaria prevê a
                                realização de treinamentos periódicos para assegurar que as boas práticas sejam seguidas em todos os
                                níveis da administração pública.
                            </p>

                            <p>
